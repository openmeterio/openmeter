package subtract

import (
	"fmt"
	"sort"
	"strings"

	"github.com/alpacahq/alpacadecimal"
	"github.com/samber/lo"

	"github.com/openmeterio/openmeter/openmeter/billing/charges/usagebased"
	"github.com/openmeterio/openmeter/openmeter/billing/models/stddetailedline"
	"github.com/openmeterio/openmeter/openmeter/productcatalog"
)

type detailedLineKey struct {
	ReferenceID string
	Category    stddetailedline.Category
	PaymentTerm productcatalog.PaymentTermType
}

type options struct {
	validateUniqueChildReferenceIDs bool
}

// Option configures SubtractRatedRunDetails.
type Option func(*options)

// WithUniqueReferenceIDValidationIgnored disables duplicate child unique
// reference ID validation for intermediate arithmetic outputs.
func WithUniqueReferenceIDValidationIgnored() Option {
	return func(o *options) {
		o.validateUniqueChildReferenceIDs = false
	}
}

// SubtractRatedRunDetails subtracts previously rated detailed lines from the
// current rated detailed lines while preserving each output line's source
// service period.
//
// Matching is based on PricerReferenceID and price-shape fields.
// PerUnitAmount is compared with decimal equality instead of being used as a
// map key, so repricing the same logical line produces a positive current line
// and a negative previous line instead of an invalid quantity-only delta.
// ChildUniqueReferenceID is generated by UniqueReferenceIDGenerator and is the
// persistence identity of output lines.
// Negative quantities are accepted as arithmetic correction inputs.
func SubtractRatedRunDetails(
	current usagebased.DetailedLines,
	previous usagebased.DetailedLines,
	uniqueReferenceIDGenerator UniqueReferenceIDGenerator,
	opts ...Option,
) (usagebased.DetailedLines, error) {
	if uniqueReferenceIDGenerator == nil {
		return nil, fmt.Errorf("unique reference id generator is required")
	}

	subtractOptions := options{
		validateUniqueChildReferenceIDs: true,
	}
	for _, opt := range opts {
		opt(&subtractOptions)
	}

	if err := validateDetailedLinesForSubtract(current); err != nil {
		return nil, fmt.Errorf("current detailed lines: %w", err)
	}

	if err := validateDetailedLinesForSubtract(previous); err != nil {
		return nil, fmt.Errorf("previous detailed lines: %w", err)
	}

	if err := validateCurrencyForSubtract(current, previous); err != nil {
		return nil, err
	}

	out, err := subtractDetailedLinesByKey(
		sumDetailedLinesByKey(current),
		sumDetailedLinesByKey(previous),
		uniqueReferenceIDGenerator,
	)
	if err != nil {
		return nil, err
	}

	if subtractOptions.validateUniqueChildReferenceIDs {
		if err := validateUniqueChildReferenceIDs(out); err != nil {
			return nil, err
		}
	}

	return out, nil
}

func validateDetailedLinesForSubtract(lines usagebased.DetailedLines) error {
	for idx, line := range lines {
		line = line.Clone()
		if line.Quantity.IsNegative() {
			line.Quantity = line.Quantity.Neg()
		}

		if err := line.Validate(); err != nil {
			return fmt.Errorf("[%d]: %w", idx, err)
		}
	}

	return nil
}

func validateCurrencyForSubtract(current, previous usagebased.DetailedLines) error {
	currentCurrencies := lo.Uniq(lo.Map(current, func(line usagebased.DetailedLine, _ int) string {
		return string(line.Currency)
	}))

	previousCurrencies := lo.Uniq(lo.Map(previous, func(line usagebased.DetailedLine, _ int) string {
		return string(line.Currency)
	}))

	if len(currentCurrencies) > 1 {
		return fmt.Errorf("current detailed lines: multiple currencies: %v", strings.Join(currentCurrencies, ", "))
	}

	if len(previousCurrencies) > 1 {
		return fmt.Errorf("previous detailed lines: multiple currencies: %v", strings.Join(previousCurrencies, ", "))
	}

	if len(currentCurrencies) == 0 || len(previousCurrencies) == 0 {
		return nil
	}

	if currentCurrencies[0] != previousCurrencies[0] {
		return fmt.Errorf("current and previous detailed lines: currency mismatch: %s != %s", currentCurrencies[0], previousCurrencies[0])
	}

	return nil
}

func sumDetailedLinesByKey(lines usagebased.DetailedLines) map[detailedLineKey]usagebased.DetailedLines {
	grouped := make(map[detailedLineKey]usagebased.DetailedLines, len(lines))

	for _, line := range lines {
		key := detailedLineKey{
			ReferenceID: line.PricerReferenceID,
			Category:    line.Category,
			PaymentTerm: line.PaymentTerm,
		}

		grouped[key] = appendOrSumDetailedLineByPerUnitAmount(grouped[key], line)
	}

	return grouped
}

func appendOrSumDetailedLineByPerUnitAmount(lines usagebased.DetailedLines, line usagebased.DetailedLine) usagebased.DetailedLines {
	for idx, existingLine := range lines {
		if !existingLine.PerUnitAmount.Equal(line.PerUnitAmount) {
			continue
		}

		lines[idx].Quantity = lines[idx].Quantity.Add(line.Quantity)
		lines[idx].Totals = lines[idx].Totals.Add(line.Totals)
		return lines
	}

	return append(lines, line.Clone())
}

func subtractDetailedLinesByKey(
	current map[detailedLineKey]usagebased.DetailedLines,
	previous map[detailedLineKey]usagebased.DetailedLines,
	generator UniqueReferenceIDGenerator,
) (usagebased.DetailedLines, error) {
	keys := lo.Union(lo.Keys(current), lo.Keys(previous))
	sort.Slice(keys, func(i, j int) bool {
		if keys[i].ReferenceID != keys[j].ReferenceID {
			return keys[i].ReferenceID < keys[j].ReferenceID
		}

		if keys[i].Category != keys[j].Category {
			return keys[i].Category < keys[j].Category
		}

		if keys[i].PaymentTerm != keys[j].PaymentTerm {
			return keys[i].PaymentTerm < keys[j].PaymentTerm
		}

		return false
	})

	out := make(usagebased.DetailedLines, 0, len(keys))

	for _, key := range keys {
		lines, err := subtractDetailedLinesWithSameKey(current[key], previous[key], generator)
		if err != nil {
			return nil, fmt.Errorf("subtract detailed lines for %s: %w", key.ReferenceID, err)
		}

		out = append(out, lines...)
	}

	return out, nil
}

func subtractDetailedLinesWithSameKey(
	current usagebased.DetailedLines,
	previous usagebased.DetailedLines,
	generator UniqueReferenceIDGenerator,
) (usagebased.DetailedLines, error) {
	out := make(usagebased.DetailedLines, 0, len(current)+len(previous))
	previousMatched := make([]bool, len(previous))

	for _, currentLine := range current {
		previousIdx, ok := findDetailedLineByPerUnitAmount(previous, currentLine.PerUnitAmount, previousMatched)
		if !ok {
			line := currentLine.Clone()
			childUniqueReferenceID, err := generator.CurrentOnly(currentLine)
			if err != nil {
				return nil, fmt.Errorf("current only child unique reference id: %w", err)
			}

			line.ChildUniqueReferenceID = childUniqueReferenceID
			if !isZeroDetailedLine(line) {
				out = append(out, line)
			}

			continue
		}

		previousMatched[previousIdx] = true

		line, ok := subtractDetailedLine(currentLine, previous[previousIdx])
		if ok {
			childUniqueReferenceID, err := generator.MatchedDelta(currentLine, previous[previousIdx])
			if err != nil {
				return nil, fmt.Errorf("matched delta child unique reference id: %w", err)
			}

			line.ChildUniqueReferenceID = childUniqueReferenceID
			out = append(out, line)
		}
	}

	for idx, previousLine := range previous {
		if previousMatched[idx] {
			continue
		}

		line := previousLine.Clone()
		line.Quantity = line.Quantity.Neg()
		line.Totals = line.Totals.Neg()
		childUniqueReferenceID, err := generator.PreviousOnlyReversal(previousLine)
		if err != nil {
			return nil, fmt.Errorf("previous only reversal child unique reference id: %w", err)
		}

		line.ChildUniqueReferenceID = childUniqueReferenceID
		if !isZeroDetailedLine(line) {
			out = append(out, line)
		}
	}

	sort.Slice(out, func(i, j int) bool {
		return out[i].PerUnitAmount.LessThan(out[j].PerUnitAmount)
	})

	return out, nil
}

func validateUniqueChildReferenceIDs(lines usagebased.DetailedLines) error {
	linesByID := lo.GroupBy(lines, func(line usagebased.DetailedLine) string {
		return line.ChildUniqueReferenceID
	})
	for id, lines := range linesByID {
		if len(lines) > 1 {
			return fmt.Errorf("duplicate child unique reference id: %s", id)
		}
	}

	return nil
}

func findDetailedLineByPerUnitAmount(lines usagebased.DetailedLines, perUnitAmount alpacadecimal.Decimal, matched []bool) (int, bool) {
	for idx, line := range lines {
		if matched[idx] {
			continue
		}

		if line.PerUnitAmount.Equal(perUnitAmount) {
			return idx, true
		}
	}

	return 0, false
}

func subtractDetailedLine(current, previous usagebased.DetailedLine) (usagebased.DetailedLine, bool) {
	line := current.Clone()
	line.Quantity = line.Quantity.Sub(previous.Quantity)
	line.Totals = line.Totals.Sub(previous.Totals)

	return line, !isZeroDetailedLine(line)
}

func isZeroDetailedLine(line usagebased.DetailedLine) bool {
	return line.Totals.IsZero()
}

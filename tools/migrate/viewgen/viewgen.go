package viewgen

import (
	"bytes"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"entgo.io/ent/dialect"
	"entgo.io/ent/dialect/entsql"
	"entgo.io/ent/entc/load"
	"github.com/go-openapi/inflect"

	"github.com/openmeterio/openmeter/pkg/strcase"
)

const DefaultOutputPath = "./tools/migrate/views.sql"

type ViewDef struct {
	Name  string
	Query string
}

func GenerateSQL(schemaPath string) ([]byte, error) {
	spec, err := (&load.Config{Path: schemaPath}).Load()
	if err != nil {
		return nil, fmt.Errorf("load ent schema: %w", err)
	}

	views, err := CollectViews(spec.Schemas, dialect.Postgres)
	if err != nil {
		return nil, fmt.Errorf("collect views: %w", err)
	}

	return RenderSQL(views), nil
}

func GenerateFile(schemaPath, outPath string) error {
	sql, err := GenerateSQL(schemaPath)
	if err != nil {
		return err
	}

	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return fmt.Errorf("create output dir: %w", err)
	}

	if err := os.WriteFile(outPath, sql, 0o644); err != nil {
		return fmt.Errorf("write output: %w", err)
	}

	return nil
}

func CollectViews(schemas []*load.Schema, dialectName string) ([]ViewDef, error) {
	views := make([]ViewDef, 0)

	for _, s := range schemas {
		if !s.View {
			continue
		}

		ant, err := entSQLAnnotation(s.Annotations)
		if err != nil {
			return nil, fmt.Errorf("%s: decode EntSQL annotation: %w", s.Name, err)
		}
		if ant == nil {
			continue
		}

		query := strings.TrimSpace(ant.ViewFor[dialectName])
		if query == "" {
			query = strings.TrimSpace(ant.ViewAs)
		}
		if query == "" {
			continue
		}

		name := ant.Table
		if name == "" {
			name = s.Config.Table
		}
		if name == "" {
			name = strcase.CamelToSnake(inflect.Pluralize(s.Name))
		}

		views = append(views, ViewDef{
			Name:  name,
			Query: query,
		})
	}

	sort.Slice(views, func(i, j int) bool {
		return views[i].Name < views[j].Name
	})

	return views, nil
}

func RenderSQL(views []ViewDef) []byte {
	var buf bytes.Buffer
	buf.WriteString("-- Code generated by viewgen, DO NOT EDIT.\n")
	buf.WriteString("-- Regenerate with: make generate-view-sql\n")
	for i, view := range views {
		if i > 0 {
			buf.WriteString("\n\n")
		}
		buf.WriteString(fmt.Sprintf("CREATE VIEW %q AS\n%s;", view.Name, view.Query))
	}
	buf.WriteByte('\n')
	return buf.Bytes()
}

func entSQLAnnotation(annotations map[string]any) (*entsql.Annotation, error) {
	raw, ok := annotations["EntSQL"]
	if !ok {
		return nil, nil
	}

	buf, err := json.Marshal(raw)
	if err != nil {
		return nil, err
	}

	var ant entsql.Annotation
	if err := json.Unmarshal(buf, &ant); err != nil {
		return nil, err
	}

	return &ant, nil
}

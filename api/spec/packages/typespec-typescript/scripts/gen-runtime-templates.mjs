// Generates src/runtime-templates.ts from the frozen baseline runtime files.
// These files are fixed boilerplate (not derived from the spec); the emitter
// copies them verbatim into the generated SDK. Run this only when the baseline
// runtime or conformance tests change.
//
// The baseline is NOT kept in the repo (it pulls vitest/vite devDeps that trip
// the workspace minimumReleaseAge constraint). Its content is already embedded
// in the committed src/runtime-templates.ts and emitted into the generated SDK,
// so `generate` does not need it. To re-run this script, restore the baseline to
// `api/spec/baseline/` first (or point BASELINE_DIR at its location).
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const baseline = process.env.BASELINE_DIR
  ? join(process.env.BASELINE_DIR, 'src')
  : join(here, '../../../baseline/src')

// outputPath (in the generated SDK) -> source file in the baseline.
// Runtime files are fixed boilerplate; the conformance tests are the spec the
// generated SDK must satisfy and are emitted alongside it so `test:sdk` runs
// against generated output.
const RUNTIME_FILES = {
  'src/core.ts': 'core.ts',
  'src/lib/types.ts': 'lib/types.ts',
  'src/lib/config.ts': 'lib/config.ts',
  'src/lib/encodings.ts': 'lib/encodings.ts',
  'src/lib/to-error.ts': 'lib/to-error.ts',
  'src/lib/request.ts': 'lib/request.ts',
  'src/models/errors.ts': 'models/errors.ts',
  'tests/client.spec.ts': '../tests/client.spec.ts',
  'tests/meters.spec.ts': '../tests/meters.spec.ts',
  'tests/errors.spec.ts': '../tests/errors.spec.ts',
  'tests/nesting.spec.ts': '../tests/nesting.spec.ts',
}

function encode(content) {
  // Base64 avoids any backtick/${} escaping hazards in the embedded source.
  return Buffer.from(content, 'utf8').toString('base64')
}

const entries = Object.entries(RUNTIME_FILES)
  .map(([outPath, srcRel]) => {
    const content = readFileSync(join(baseline, srcRel), 'utf8')
    return `  ${JSON.stringify(outPath)}: '${encode(content)}',`
  })
  .join('\n')

const out = `// Code generated by scripts/gen-runtime-templates.mjs. DO NOT EDIT.
// Static SDK runtime files, copied verbatim from the baseline into the
// generated SDK. Base64-encoded to avoid escaping hazards.

const ENCODED: Record<string, string> = {
${entries}
}

export const RUNTIME_TEMPLATES: Record<string, string> = Object.fromEntries(
  Object.entries(ENCODED).map(([path, b64]) => [
    path,
    Buffer.from(b64, 'base64').toString('utf8'),
  ]),
)
`

writeFileSync(join(here, '../src/runtime-templates.ts'), out)
console.log(
  `Wrote src/runtime-templates.ts (${entries.split('\n').length} files)`,
)

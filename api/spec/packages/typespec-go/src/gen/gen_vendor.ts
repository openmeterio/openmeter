/**
 * Read the vendored static template tree at `src/templates/` and emit each
 * file with the module path rewritten to the consumer's `module` option.
 *
 * Only `.go` files (excluding `_test.go`) are emitted. NOTICE and LICENSE
 * are not emitted into the SDK; they live in the emitter package only.
 */
import { readdirSync, readFileSync, statSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

import { GENERATED_BANNER } from "./writer.js";

const REFERENCE_MODULE = "github.com/openmeterio/openmeter-go-sdk";

/**
 * Banners in the vendored source that we replace with our own. The Speakeasy
 * attribution is removed because the vendored code ships as part of an SDK
 * generated by `@openmeter/typespec-go`, not by Speakeasy.
 */
const SPEAKEASY_BANNER_PATTERNS: ReadonlyArray<RegExp> = [
  /^\/\/ Code generated by Speakeasy \(https:\/\/speakeasy\.com\)\. DO NOT EDIT\.\s*\n/,
  /^\/\/ Code generated by Speakeasy[^\n]*\n/,
];

export interface VendoredFile {
  /** Path relative to the emitter output dir (e.g. "internal/utils/json.go"). */
  readonly path: string;
  readonly content: string;
}

/**
 * The emitter source ships its templates under `src/templates/` when developing
 * and `dist/templates/` when packaged. Detect both.
 */
function resolveTemplateRoot(): string {
  // `import.meta.url` will be inside `dist/` at runtime (post-alloy-build).
  // The templates directory is at `src/templates/` relative to the package root.
  // Walk up from the emitter file location to find the package directory.
  const here = dirname(fileURLToPath(import.meta.url));
  // Try dist/templates first, then src/templates.
  const candidates = [
    join(here, "..", "templates"),
    join(here, "..", "..", "src", "templates"),
    join(here, "..", "..", "templates"),
  ];
  for (const c of candidates) {
    try {
      if (statSync(c).isDirectory()) return c;
    } catch {
      // try next
    }
  }
  throw new Error(
    `Vendored template root not found. Tried: ${candidates.join(", ")}`,
  );
}

export function emitVendoredFiles(module: string): VendoredFile[] {
  const root = resolveTemplateRoot();
  const out: VendoredFile[] = [];
  walk(root, root, (full, rel) => {
    if (!rel.endsWith(".go")) return;
    if (rel.endsWith("_test.go")) return;
    let src = readFileSync(full, "utf8");
    src = src.replaceAll(REFERENCE_MODULE, module);
    src = rewriteBanner(src);
    out.push({ path: rel.split("\\").join("/"), content: src });
  });
  return out;
}

/**
 * Replace the Speakeasy "Code generated" banner with our own. If no banner
 * was present (rare in the reference tree), prepend ours so downstream tooling
 * still recognizes the file as generated.
 */
function rewriteBanner(src: string): string {
  for (const pattern of SPEAKEASY_BANNER_PATTERNS) {
    if (pattern.test(src)) {
      return src.replace(pattern, `${GENERATED_BANNER}\n`);
    }
  }
  return `${GENERATED_BANNER}\n\n${src}`;
}

function walk(
  root: string,
  dir: string,
  visit: (full: string, rel: string) => void,
): void {
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const s = statSync(full);
    if (s.isDirectory()) {
      walk(root, full, visit);
    } else {
      visit(full, relative(root, full));
    }
  }
}
